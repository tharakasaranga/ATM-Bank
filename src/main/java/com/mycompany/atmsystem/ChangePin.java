    public boolean checkNewPin(String PinNew){
       int length = PinNew.length();
       int Pin = Integer.parseInt(PinNew);       
        
       if(Pin<=9999 && length==4){
            return true;
        }
       else{
           JOptionPane.showMessageDialog(rootPane, "Please follow the instructions...");
       }
        return false;
    }
    
