    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Database database = new Database();
        String pin = tpin.getText();
        
        if(database.checkPin(pin)==true && nextFrame.equals("Login")){
            checkBalance1 next = new checkBalance1();
            next.setPinNumber(pin);
            JOptionPane.showMessageDialog(rootPane, "Welcome..");
            next.setVisible(true);
            this.dispose();
        }
        else if(database.checkPin(pin)==true && nextFrame.equals("Bill")){
            BillPay next = new BillPay();
       
            
            String accountNo = database.getAcno(pin);
            double balance = database.getBalance(pin);
            next.setAcNo(accountNo);
            next.setAccountBalance(balance);
            JOptionPane.showMessageDialog(rootPane, "Welcome..");
            next.setVisible(true);
            this.dispose();
        }  
       
             
        else if(database.checkPin(pin)==true && nextFrame.equals("Deposit")){
            Deposit1 next = new Deposit1();
            next.setDetails(pin);
            
            next.setVisible(true);
            this.dispose();
        }
             
        
        else if(database.checkPin(pin)==true && nextFrame.equals("Withdraw")){
            Withdraw withdrawal = new Withdraw();
          withdrawal.setDetails(pin);
            withdrawal.setVisible(true);
            this.dispose();
        }
        
        else if(database.checkPin(pin)==true && nextFrame.equals("ChangePin")){
                       
            ChangePin change = new ChangePin();
            change.setOldPin(pin);
            change.setVisible(true);
            this.dispose();
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Wrong Pin Number.Enter valid Pin Number");
            
        }
        
        
       
    }//GEN-LAST:event_jButton2ActionPerformed

